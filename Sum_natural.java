public class Sum_natural			// class name Sum_natural
{
	public static void main(String args[])	// program starts here
	{
		int i, len=10, sum=0;	// initializing variables

		for(i=0; i<=len; i++)	// for loop
		{
			sum = sum + i;

		}
		System.out.println("Sum of natural nummmbers: "+ sum);	//printing the statement
	}
}
